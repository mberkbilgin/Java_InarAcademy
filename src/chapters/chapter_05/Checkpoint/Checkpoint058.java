package chapters.chapter_05.Checkpoint;

public class Checkpoint058 {

	public static void main(String[] args) {
		int sumA = 0;

		for (int i = 0; i < 10; ++i) {
			sumA += i;
		}
		System.out.println("sumA is "+sumA);
		
		int sumB = 0;
		for (int i = 0; i < 10; i++) {
			sumB += i;
		}
		System.out.println("sumB is "+sumB);
	}

}
