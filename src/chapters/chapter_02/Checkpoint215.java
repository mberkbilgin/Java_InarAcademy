package chapters.chapter_02;

public class Checkpoint215 {

	public static void main(String[] args) {
		System.out.println("25 / 4 is " + 25 / 4); // shown as int
		System.out.println("25 / 4.0 is " + 25 / 4.0); // shown as double
		System.out.println("3 * 2 / 4 is " + 3 * 2 / 4); // shown as int
		System.out.println("3.0 * 2 / 4 is " + 3.0 * 2 / 4); //shown as double

	}

}
